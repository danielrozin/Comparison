// ---------------------------------------------------------------------------

export async function getComparisonHistory(
  slug: string
): Promise<
  {
    id: string;
    action: string;
    changes: unknown;
    performedBy: string | null;
    createdAt: string;
  }[]
> {
  const prisma = getPrismaClient();
  if (!prisma) return [];

  try {
    const comp = await prisma.comparison.findUnique({
      where: { slug },
      select: { id: true },
    });
    if (!comp) return [];

    const logs = await prisma.changeLog.findMany({
      where: { comparisonId: comp.id },
      orderBy: { createdAt: "desc" },
    });

    return logs.map(
      (l: {
        id: string;
        action: string;
        changes: unknown;
        performedBy: string | null;
        createdAt: Date;
      }) => ({
        id: l.id,
        action: l.action,
        changes: l.changes,
        performedBy: l.performedBy,
        createdAt: l.createdAt.toISOString(),
      })
    );
  } catch (e) {
    console.warn("getComparisonHistory failed:", e);
    return [];
  }
}

