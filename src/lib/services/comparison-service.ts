export async function getComparisonBySlug(
  slug: string
): Promise<ComparisonPageData | null> {
  // Check cache first
  const cacheKey = `comparison:${slug}`;
  const cached = await getFromCache<ComparisonPageData>(cacheKey);
  if (cached) return cached;

  const prisma = getPrismaClient();
  if (prisma) {
    try {
      const row = await prisma.comparison.findUnique({
        where: { slug },
        include: COMPARISON_INCLUDE,
      });
      if (row) {
        // Use the linking engine for smarter related suggestions
        const entitySlugs = (row as PrismaComparisonRow).entities.map(
          (ce) => ce.entity.slug
        );
        const [linked, blogPosts] = await Promise.all([
          getLinkedComparisons({
            comparisonId: row.id,
            slug: row.slug,
            category: row.category,
            entitySlugs,
          }),
          getRelatedBlogPosts(row.slug, row.category, 5),
        ]);
        const related: RelatedComparison[] = linked.map((l) => ({
          slug: l.slug,
          title: l.title,
          category: l.category,
        }));
        const result = transformToPageData(row as PrismaComparisonRow, related, blogPosts);
        await setCache(cacheKey, result, CACHE_TTL_COMPARISON);
        return result;
      }
    } catch (e) {
      console.warn("Prisma query failed for getComparisonBySlug, falling back to mock:", e);
    }
  }

  // Mock fallback — also use the linking engine
  const mock = getMockComparison(slug);
  if (mock) {
    const linked = await getLinkedComparisons({
      comparisonId: mock.id,
      slug: mock.slug,
      category: mock.category,
      entitySlugs: mock.entities.map((e) => e.slug),
    });
    mock.relatedComparisons = linked.map((l) => ({
      slug: l.slug,
      title: l.title,
      category: l.category,
    }));
    mock.relatedBlogPosts = [];
    await setCache(cacheKey, mock, CACHE_TTL_COMPARISON);
  }
  return mock;
}

/**
 * Batch check which slugs exist in the comparisons table.
 * Returns only the slugs that have matching records — single query, no N+1.
 */
export async function getComparisonSlugsExisting(
  slugs: string[]
): Promise<string[]> {
  if (slugs.length === 0) return [];

  const prisma = getPrismaClient();
  if (!prisma) {
    // Fall back to mock data check
    return slugs.filter((slug) => getMockComparison(slug) !== null);
  }

  try {
    const rows = await prisma.comparison.findMany({
      where: { slug: { in: slugs } },
      select: { slug: true },
    });
    const dbSlugs = rows.map((r: { slug: string }) => r.slug);
    // Also check mock data for any slugs not in DB
    const mockSlugs = slugs.filter(
      (s) => !dbSlugs.includes(s) && getMockComparison(s) !== null
    );
    return [...dbSlugs, ...mockSlugs];
  } catch {
    return slugs.filter((slug) => getMockComparison(slug) !== null);
  }
}

/**
 * Batch fetch comparison titles by slugs.
 * Returns a map of slug -> title for existing comparisons.
 */
export async function getComparisonTitlesBySlugs(
  slugs: string[]
): Promise<Record<string, string>> {
  if (slugs.length === 0) return {};

  const result: Record<string, string> = {};
  const prisma = getPrismaClient();

  if (prisma) {
    try {
      const rows = await prisma.comparison.findMany({
        where: { slug: { in: slugs } },
        select: { slug: true, title: true },
      });
      for (const r of rows) {
        result[r.slug] = r.title;
      }
    } catch {
      // fall through to mock
    }
  }

  // Fill in any missing from mock data
  for (const slug of slugs) {
    if (!result[slug]) {
      const mock = getMockComparison(slug);
      if (mock) result[slug] = mock.title;
    }
  }

  return result;
}

export async function getTrendingComparisons(
  limit: number = 10
): Promise<TrendingComparison[]> {
  const cacheKey = `trending:${limit}`;
  const cached = await getFromCache<TrendingComparison[]>(cacheKey);
  if (cached) return cached;

  const prisma = getPrismaClient();
  if (prisma) {
    try {
      const rows = await prisma.comparison.findMany({
        where: { status: "published" },
        orderBy: { viewCount: "desc" },
        take: limit,
        include: {
          entities: {
            include: {
              entity: { select: { imageUrl: true } },
            },
            orderBy: { position: "asc" },
          },
        },
      });
      if (rows.length > 0) {
        const result = rows.map(
          (r: {
            slug: string;
            title: string;
            category: string | null;
            viewCount: number;
            entities: { entity: { imageUrl: string | null } }[];
          }) => ({
            slug: r.slug,
            title: r.title,
            category: r.category || "general",
            viewCount: r.viewCount,
            entityImages: r.entities
              .map((e: { entity: { imageUrl: string | null } }) => e.entity.imageUrl)
              .filter(Boolean) as string[],
          })
        );
        await setCache(cacheKey, result, CACHE_TTL_TRENDING);
        return result;
      }
    } catch (e) {
      console.warn("Prisma query failed for getTrendingComparisons, falling back to mock:", e);
    }
  }
  const mock = getMockTrending(limit);
  await setCache(cacheKey, mock, CACHE_TTL_TRENDING);
  return mock;
}

export async function getRelatedComparisons(
  comparisonId: string,
  limit: number = 8
): Promise<RelatedComparison[]> {
  const cacheKey = `related:${comparisonId}:${limit}`;
  const cached = await getFromCache<RelatedComparison[]>(cacheKey);
  if (cached) return cached;

  const prisma = getPrismaClient();
  if (prisma) {
    try {
      const comp = await prisma.comparison.findUnique({
        where: { id: comparisonId },
        select: {
          slug: true,
          category: true,
          relatedComparisonIds: true,
          entities: {
            select: { entity: { select: { slug: true } } },
          },
        },
      });
      if (comp) {
        const linked = await getLinkedComparisons({
          comparisonId,
          slug: comp.slug,
          category: comp.category,
          entitySlugs: comp.entities.map((ce: { entity: { slug: string } }) => ce.entity.slug),
        }, limit);
        const result = linked.map((l) => ({ slug: l.slug, title: l.title, category: l.category }));
        await setCache(cacheKey, result, CACHE_TTL_TRENDING);
        return result;
      }
    } catch (e) {
      console.warn("Prisma query failed for getRelatedComparisons, falling back to mock:", e);
    }
  }
  const mock = getMockRelated(comparisonId, limit);
  await setCache(cacheKey, mock, CACHE_TTL_TRENDING);
  return mock;
}

async function getRelatedFromDb(
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  prisma: any,
  comp: { category?: string | null; relatedComparisonIds?: string[] },
  limit: number = 8
): Promise<RelatedComparison[]> {
  // First try explicit related IDs
  if (comp.relatedComparisonIds && comp.relatedComparisonIds.length > 0) {
    const related = await prisma.comparison.findMany({
      where: { id: { in: comp.relatedComparisonIds } },
      select: { slug: true, title: true, category: true },
      take: limit,
    });
    if (related.length > 0) return related;
  }
  // Fallback: same category
  if (comp.category) {
    const related = await prisma.comparison.findMany({
      where: { category: comp.category, status: "published" },
      select: { slug: true, title: true, category: true },
      take: limit,
    });
    return related;
  }
  return [];
}

export async function searchComparisons(
  query: string,
  limit: number = 20
): Promise<{ slug: string; title: string; category: string; viewCount: number }[]> {
  const searchCacheKey = `search:${query.toLowerCase().trim()}:${limit}`;
  const cached = await getFromCache<{ slug: string; title: string; category: string; viewCount: number }[]>(searchCacheKey);
  if (cached) return cached;

  const prisma = getPrismaClient();
  if (prisma) {
    try {
      const lower = query.toLowerCase();
      const rows = await prisma.comparison.findMany({
        where: {
          OR: [
            { title: { contains: lower, mode: "insensitive" } },
            { slug: { contains: lower.replace(/\s+/g, "-") } },
            { category: { contains: lower, mode: "insensitive" } },
          ],
        },
        select: {
          slug: true,
          title: true,
          category: true,
          viewCount: true,
        },
        orderBy: { viewCount: "desc" },
        take: limit,
      });
      if (rows.length > 0) {
        return rows.map(
          (r: { slug: string; title: string; category: string | null; viewCount: number }) => ({
            slug: r.slug,
            title: r.title,
            category: r.category || "general",
            viewCount: r.viewCount,
          })
        );
      }
    } catch (e) {
      console.warn("Prisma query failed for searchComparisons, falling back to mock:", e);
    }
  }

  // Mock fallback
  const allSlugs = getAllMockSlugs();
  const lower = query.toLowerCase();
  const results: { slug: string; title: string; category: string; viewCount: number }[] = [];

  for (const slug of allSlugs) {
    const comp = getMockComparison(slug);
    if (!comp) continue;
    const titleLower = comp.title.toLowerCase();
    const catLower = (comp.category || "").toLowerCase();
    if (
      titleLower.includes(lower) ||
      catLower.includes(lower) ||
      slug.includes(lower.replace(/\s+/g, "-")) ||
      comp.entities.some((e) => e.name.toLowerCase().includes(lower))
    ) {
      results.push({
        slug: comp.slug,
        title: comp.title,
        category: comp.category || "general",
        viewCount: comp.metadata.viewCount,
      });
    }
  }

  results.sort((a, b) => {
    const aExact = a.title.toLowerCase().includes(lower) ? 1 : 0;
    const bExact = b.title.toLowerCase().includes(lower) ? 1 : 0;
    if (aExact !== bExact) return bExact - aExact;
    return b.viewCount - a.viewCount;
  });

  const finalResults = results.slice(0, limit);
  await setCache(searchCacheKey, finalResults, CACHE_TTL_SEARCH);
  return finalResults;
}

export async function incrementViewCount(comparisonId: string): Promise<void> {
  const prisma = getPrismaClient();
  if (prisma) {
    try {
      await prisma.comparison.update({
        where: { id: comparisonId },
        data: { viewCount: { increment: 1 } },
      });
      return;
    } catch (e) {
      console.warn("Prisma incrementViewCount failed:", e);
    }
  }
  console.log(`View count incremented for ${comparisonId} (mock)`);
}

export async function getComparisonsByCategory(
  category: string,
  limit: number = 20,
  offset: number = 0
): Promise<{ comparisons: RelatedComparison[]; total: number }> {
  const prisma = getPrismaClient();
  if (prisma) {
    try {
      const [rows, total] = await Promise.all([
        prisma.comparison.findMany({
          where: { category: { equals: category, mode: "insensitive" } },
          select: { slug: true, title: true, category: true },
          orderBy: { viewCount: "desc" },
          skip: offset,
          take: limit,
        }),
        prisma.comparison.count({
          where: { category: { equals: category, mode: "insensitive" } },
        }),
      ]);
      if (rows.length > 0 || total > 0) {
        return { comparisons: rows, total };
      }
    } catch (e) {
      console.warn("Prisma getComparisonsByCategory failed:", e);
    }
  }

  const all = getMockComparisonsByCategory(category);
  const comparisons = all.map((comp) => ({
    slug: comp.slug,
    title: comp.title,
    category: comp.category,
  }));
  return {
    comparisons: comparisons.slice(offset, offset + limit),
    total: comparisons.length,
  };
}

