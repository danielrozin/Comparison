export async function getComparisonBySlug(
  slug: string
): Promise<ComparisonPageData | null> {
  const prisma = getPrismaClient();
  if (prisma) {
    try {
      const row = await prisma.comparison.findUnique({
        where: { slug },
        include: COMPARISON_INCLUDE,
      });
      if (row) {
        // Fetch related comparisons
        const related = await getRelatedFromDb(prisma, row);
        return transformToPageData(row as PrismaComparisonRow, related);
      }
    } catch (e) {
      console.warn("Prisma query failed for getComparisonBySlug, falling back to mock:", e);
    }
  }
  return getMockComparison(slug);
}

export async function getTrendingComparisons(
  limit: number = 10
): Promise<TrendingComparison[]> {
  const prisma = getPrismaClient();
  if (prisma) {
    try {
      const rows = await prisma.comparison.findMany({
        where: { status: "published" },
        orderBy: { viewCount: "desc" },
        take: limit,
        include: {
          entities: {
            include: {
              entity: { select: { imageUrl: true } },
            },
            orderBy: { position: "asc" },
          },
        },
      });
      if (rows.length > 0) {
        return rows.map(
          (r: {
            slug: string;
            title: string;
            category: string | null;
            viewCount: number;
            entities: { entity: { imageUrl: string | null } }[];
          }) => ({
            slug: r.slug,
            title: r.title,
            category: r.category || "general",
            viewCount: r.viewCount,
            entityImages: r.entities
              .map((e: { entity: { imageUrl: string | null } }) => e.entity.imageUrl)
              .filter(Boolean) as string[],
          })
        );
      }
    } catch (e) {
      console.warn("Prisma query failed for getTrendingComparisons, falling back to mock:", e);
    }
  }
  return getMockTrending(limit);
}

export async function getRelatedComparisons(
  comparisonId: string,
  limit: number = 8
): Promise<RelatedComparison[]> {
  const prisma = getPrismaClient();
  if (prisma) {
    try {
      const comp = await prisma.comparison.findUnique({
        where: { id: comparisonId },
        select: { category: true, relatedComparisonIds: true },
      });
      if (comp) {
        return getRelatedFromDb(prisma, comp, limit);
      }
    } catch (e) {
      console.warn("Prisma query failed for getRelatedComparisons, falling back to mock:", e);
    }
  }
  return getMockRelated(comparisonId, limit);
}

async function getRelatedFromDb(
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  prisma: any,
  comp: { category?: string | null; relatedComparisonIds?: string[] },
  limit: number = 8
): Promise<RelatedComparison[]> {
  // First try explicit related IDs
  if (comp.relatedComparisonIds && comp.relatedComparisonIds.length > 0) {
    const related = await prisma.comparison.findMany({
      where: { id: { in: comp.relatedComparisonIds } },
      select: { slug: true, title: true, category: true },
      take: limit,
    });
    if (related.length > 0) return related;
  }
  // Fallback: same category
  if (comp.category) {
    const related = await prisma.comparison.findMany({
      where: { category: comp.category, status: "published" },
      select: { slug: true, title: true, category: true },
      take: limit,
    });
    return related;
  }
  return [];
}

export async function searchComparisons(
  query: string,
  limit: number = 20
): Promise<{ slug: string; title: string; category: string; viewCount: number }[]> {
  const prisma = getPrismaClient();
  if (prisma) {
    try {
      const lower = query.toLowerCase();
      const rows = await prisma.comparison.findMany({
        where: {
          OR: [
            { title: { contains: lower, mode: "insensitive" } },
            { slug: { contains: lower.replace(/\s+/g, "-") } },
            { category: { contains: lower, mode: "insensitive" } },
          ],
        },
        select: {
          slug: true,
          title: true,
          category: true,
          viewCount: true,
        },
        orderBy: { viewCount: "desc" },
        take: limit,
      });
      if (rows.length > 0) {
        return rows.map(
          (r: { slug: string; title: string; category: string | null; viewCount: number }) => ({
            slug: r.slug,
            title: r.title,
            category: r.category || "general",
            viewCount: r.viewCount,
          })
        );
      }
    } catch (e) {
      console.warn("Prisma query failed for searchComparisons, falling back to mock:", e);
    }
  }

  // Mock fallback
  const allSlugs = getAllMockSlugs();
  const lower = query.toLowerCase();
  const results: { slug: string; title: string; category: string; viewCount: number }[] = [];

  for (const slug of allSlugs) {
    const comp = getMockComparison(slug);
    if (!comp) continue;
    const titleLower = comp.title.toLowerCase();
    const catLower = (comp.category || "").toLowerCase();
    if (
      titleLower.includes(lower) ||
      catLower.includes(lower) ||
      slug.includes(lower.replace(/\s+/g, "-")) ||
      comp.entities.some((e) => e.name.toLowerCase().includes(lower))
    ) {
      results.push({
        slug: comp.slug,
        title: comp.title,
        category: comp.category || "general",
        viewCount: comp.metadata.viewCount,
      });
    }
  }

  results.sort((a, b) => {
    const aExact = a.title.toLowerCase().includes(lower) ? 1 : 0;
    const bExact = b.title.toLowerCase().includes(lower) ? 1 : 0;
    if (aExact !== bExact) return bExact - aExact;
    return b.viewCount - a.viewCount;
  });

  return results.slice(0, limit);
}

export async function incrementViewCount(comparisonId: string): Promise<void> {
  const prisma = getPrismaClient();
  if (prisma) {
    try {
      await prisma.comparison.update({
        where: { id: comparisonId },
        data: { viewCount: { increment: 1 } },
      });
      return;
    } catch (e) {
      console.warn("Prisma incrementViewCount failed:", e);
    }
  }
  console.log(`View count incremented for ${comparisonId} (mock)`);
}

export async function getComparisonsByCategory(
  category: string,
  limit: number = 20,
  offset: number = 0
): Promise<{ comparisons: RelatedComparison[]; total: number }> {
  const prisma = getPrismaClient();
  if (prisma) {
    try {
      const [rows, total] = await Promise.all([
        prisma.comparison.findMany({
          where: { category: { equals: category, mode: "insensitive" } },
          select: { slug: true, title: true, category: true },
          orderBy: { viewCount: "desc" },
          skip: offset,
          take: limit,
        }),
        prisma.comparison.count({
          where: { category: { equals: category, mode: "insensitive" } },
        }),
      ]);
      if (rows.length > 0 || total > 0) {
        return { comparisons: rows, total };
      }
    } catch (e) {
      console.warn("Prisma getComparisonsByCategory failed:", e);
    }
  }

  const all = getMockComparisonsByCategory(category);
  const comparisons = all.map((comp) => ({
    slug: comp.slug,
    title: comp.title,
    category: comp.category,
  }));
  return {
    comparisons: comparisons.slice(offset, offset + limit),
    total: comparisons.length,
  };
}

