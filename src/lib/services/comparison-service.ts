// Lazy-import prisma to avoid crash when DATABASE_URL is not set
function getPrismaClient() {
  try {
    // eslint-disable-next-line @typescript-eslint/no-require-imports
    const { getPrisma } = require("@/lib/db/prisma");
    return getPrisma();
  } catch {
    return null;
  }
}

// ---------------------------------------------------------------------------
// Helpers: transform Prisma rows into ComparisonPageData
// ---------------------------------------------------------------------------

interface PrismaComparisonRow {
  id: string;
  slug: string;
  title: string;
  shortAnswer: string | null;
  keyDifferences: unknown;
  verdict: string | null;
  category: string | null;
  metaTitle: string | null;
  metaDescription: string | null;
  publishedAt: Date | null;
  updatedAt: Date;
  isAutoGenerated: boolean;
  isHumanReviewed: boolean;
  viewCount: number;
  relatedComparisonIds: string[];
  entities: {
    id: string;
    position: number;
    pros: string[];
    cons: string[];
    bestFor: string | null;
    entity: {
      id: string;
      slug: string;
      name: string;
      shortDesc: string | null;
      imageUrl: string | null;
      entityType: { slug: string };
      attributeValues: {
        id: string;
        valueText: string | null;
        valueNumber: number | null;
        valueBoolean: boolean | null;
        attribute: {
          id: string;
          slug: string;
          name: string;
          unit: string | null;
          category: string | null;
          dataType: string;
          higherIsBetter: boolean | null;
        };
      }[];
    };
  }[];
  faqs: { question: string; answer: string }[];
}

function transformToPageData(
  row: PrismaComparisonRow,
  relatedComps: RelatedComparison[] = []
): ComparisonPageData {
  // Build entities
  const entities: ComparisonEntityData[] = row.entities
    .sort((a, b) => a.position - b.position)
    .map((ce) => ({
      id: ce.entity.id,
      slug: ce.entity.slug,
      name: ce.entity.name,
      shortDesc: ce.entity.shortDesc,
      imageUrl: ce.entity.imageUrl,
      entityType: ce.entity.entityType.slug,
      position: ce.position,
      pros: ce.pros,
      cons: ce.cons,
      bestFor: ce.bestFor,
    }));

  // Build attributes from entity attribute values
  const attributeMap = new Map<
    string,
    {
      attr: {
        id: string;
        slug: string;
        name: string;
        unit: string | null;
        category: string | null;
        dataType: string;
        higherIsBetter: boolean | null;
      };
      values: {
        entityId: string;
        valueText: string | null;
        valueNumber: number | null;
        valueBoolean: boolean | null;
        winner?: boolean;
      }[];
    }
  >();

  for (const ce of row.entities) {
    for (const av of ce.entity.attributeValues) {
      if (!attributeMap.has(av.attribute.id)) {
        attributeMap.set(av.attribute.id, {
          attr: av.attribute,
          values: [],
        });
      }
      const entry = attributeMap.get(av.attribute.id)!;
      entry.values.push({
        entityId: ce.entity.id,
        valueText: av.valueText,
        valueNumber: av.valueNumber,
        valueBoolean: av.valueBoolean,
      });
    }
  }

  // Determine winners for numeric comparable attributes
  const attributes: ComparisonAttribute[] = Array.from(
    attributeMap.values()
  ).map(({ attr, values }) => {
    // Set winner flags for numeric attributes
    if (
      attr.higherIsBetter !== null &&
      values.length === 2 &&
      values[0].valueNumber !== null &&
      values[1].valueNumber !== null
    ) {
      const [v0, v1] = values;
      const higher = attr.higherIsBetter;
      if (v0.valueNumber! > v1.valueNumber!) {
        v0.winner = higher;
        v1.winner = !higher;
      } else if (v1.valueNumber! > v0.valueNumber!) {
        v1.winner = higher;
        v0.winner = !higher;
      }
    }
    return {
      id: attr.id,
      slug: attr.slug,
      name: attr.name,
      unit: attr.unit,
      category: attr.category,
      dataType: attr.dataType,
      higherIsBetter: attr.higherIsBetter,
      values,
    };
  });

  const faqs: FAQData[] = row.faqs.map((f) => ({
    question: f.question,
    answer: f.answer,
  }));

  const keyDifferences = Array.isArray(row.keyDifferences)
    ? (row.keyDifferences as ComparisonPageData["keyDifferences"])
    : [];

  return {
    id: row.id,
    slug: row.slug,
    title: row.title,
    shortAnswer: row.shortAnswer,
    keyDifferences,
    verdict: row.verdict,
    category: row.category,
    entities,
    attributes,
    faqs,
    relatedComparisons: relatedComps,
    metadata: {
      metaTitle:
        row.metaTitle || `${row.title}: Complete Comparison | Comparison`,
      metaDescription:
        row.metaDescription || row.shortAnswer?.slice(0, 155) || "",
      publishedAt: row.publishedAt?.toISOString() ?? null,
      updatedAt: row.updatedAt.toISOString(),
      isAutoGenerated: row.isAutoGenerated,
      isHumanReviewed: row.isHumanReviewed,
      viewCount: row.viewCount,
    },
  };
}

const COMPARISON_INCLUDE = {
  entities: {
    include: {
      entity: {
        include: {
          entityType: true,
          attributeValues: {
            include: { attribute: true },
          },
        },
      },
    },
  },
  faqs: true,
};

