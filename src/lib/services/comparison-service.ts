export async function saveComparison(
  data: ComparisonPageData
): Promise<{ id: string } | null> {
  const prisma = getPrismaClient();
  if (!prisma) {
    console.warn("saveComparison: no database connection, skipping persist");
    return null;
  }

  try {
    // Determine entity type slug from data (default to "general")
    const entityTypeSlugs = [
      ...new Set(data.entities.map((e) => e.entityType || "general")),
    ];

    // Upsert EntityTypes
    const entityTypeMap = new Map<string, string>();
    for (const slug of entityTypeSlugs) {
      const et = await prisma.entityType.upsert({
        where: { slug },
        create: {
          slug,
          name: slug.charAt(0).toUpperCase() + slug.slice(1),
        },
        update: {},
      });
      entityTypeMap.set(slug, et.id);
    }

    // Upsert Entities
    const entityIdMap = new Map<string, string>(); // old id -> new db id
    for (const ent of data.entities) {
      const typeId = entityTypeMap.get(ent.entityType || "general")!;
      const dbEntity = await prisma.entity.upsert({
        where: { slug: ent.slug },
        create: {
          slug: ent.slug,
          name: ent.name,
          shortDesc: ent.shortDesc,
          imageUrl: ent.imageUrl,
          entityTypeId: typeId,
          status: "published",
        },
        update: {
          name: ent.name,
          shortDesc: ent.shortDesc,
          imageUrl: ent.imageUrl,
        },
      });
      entityIdMap.set(ent.id, dbEntity.id);
    }

    // Upsert Comparison
    const comparison = await prisma.comparison.upsert({
      where: { slug: data.slug },
      create: {
        slug: data.slug,
        title: data.title,
        shortAnswer: data.shortAnswer,
        keyDifferences: data.keyDifferences as unknown as object,
        verdict: data.verdict,
        category: data.category,
        metaTitle: data.metadata.metaTitle,
        metaDescription: data.metadata.metaDescription,
        status: "published",
        isAutoGenerated: data.metadata.isAutoGenerated,
        isHumanReviewed: data.metadata.isHumanReviewed,
        viewCount: data.metadata.viewCount || 0,
        publishedAt: data.metadata.publishedAt
          ? new Date(data.metadata.publishedAt)
          : new Date(),
        relatedComparisonIds: data.relatedComparisons.map((r) => r.slug),
      },
      update: {
        title: data.title,
        shortAnswer: data.shortAnswer,
        keyDifferences: data.keyDifferences as unknown as object,
        verdict: data.verdict,
        category: data.category,
        metaTitle: data.metadata.metaTitle,
        metaDescription: data.metadata.metaDescription,
        isAutoGenerated: data.metadata.isAutoGenerated,
        isHumanReviewed: data.metadata.isHumanReviewed,
        viewCount: data.metadata.viewCount || 0,
      },
    });

    // Clear existing join records for this comparison (to avoid duplicates on update)
    await prisma.comparisonEntity.deleteMany({
      where: { comparisonId: comparison.id },
    });
    await prisma.fAQ.deleteMany({
      where: { comparisonId: comparison.id },
    });

    // Create ComparisonEntity join records
    for (const ent of data.entities) {
      const dbEntityId = entityIdMap.get(ent.id)!;
      await prisma.comparisonEntity.create({
        data: {
          comparisonId: comparison.id,
          entityId: dbEntityId,
          position: ent.position,
          pros: ent.pros,
          cons: ent.cons,
          bestFor: ent.bestFor,
        },
      });
    }

    // Create Attributes and AttributeValues
    for (const attr of data.attributes) {
      // Find the entity type for this attribute (use first entity's type)
      const firstEntityTypeSlug =
        data.entities[0]?.entityType || "general";
      const typeId = entityTypeMap.get(firstEntityTypeSlug)!;

      const dbAttr = await prisma.attribute.upsert({
        where: {
          slug_entityTypeId: {
            slug: attr.slug,
            entityTypeId: typeId,
          },
        },
        create: {
          slug: attr.slug,
          name: attr.name,
          unit: attr.unit,
          category: attr.category,
          dataType: attr.dataType,
          higherIsBetter: attr.higherIsBetter,
          entityTypeId: typeId,
        },
        update: {
          name: attr.name,
          unit: attr.unit,
          category: attr.category,
          higherIsBetter: attr.higherIsBetter,
        },
      });

      // Create attribute values for each entity
      for (const val of attr.values) {
        const dbEntityId = entityIdMap.get(val.entityId);
        if (!dbEntityId) continue;

        await prisma.attributeValue.upsert({
          where: {
            entityId_attributeId: {
              entityId: dbEntityId,
              attributeId: dbAttr.id,
            },
          },
          create: {
            entityId: dbEntityId,
            attributeId: dbAttr.id,
            valueText: val.valueText,
            valueNumber: val.valueNumber,
            valueBoolean: val.valueBoolean,
          },
          update: {
            valueText: val.valueText,
            valueNumber: val.valueNumber,
            valueBoolean: val.valueBoolean,
          },
        });
      }
    }

    // Create FAQ records
    for (const faq of data.faqs) {
      await prisma.fAQ.create({
        data: {
          question: faq.question,
          answer: faq.answer,
          comparisonId: comparison.id,
        },
      });
    }

    // Create ChangeLog entry
    await prisma.changeLog.create({
      data: {
        entityType: "comparison",
        entityId: comparison.id,
        action: "created",
        changes: {
          title: data.title,
          slug: data.slug,
          category: data.category,
          entityCount: data.entities.length,
          attributeCount: data.attributes.length,
          faqCount: data.faqs.length,
        },
        comparisonId: comparison.id,
      },
    });

    // Invalidate cached comparison and trending list
    await invalidateCache(`comparison:${data.slug}`);
    await invalidateCache(`trending:10`);

    return { id: comparison.id };
  } catch (e) {
    console.error("saveComparison failed:", e);
    return null;
  }
}

