export interface SearchResult {
  type: "comparison" | "entity" | "category";
  slug: string;
  title: string;
  subtitle?: string;
  imageUrl?: string;
  category?: string;
}

export interface TrendingComparison {
  slug: string;
  title: string;
  category: string;
  viewCount: number;
  entityImages: string[];
  updatedAt?: string | null;
}

export interface CategoryData {
  slug: string;
  name: string;
  description: string | null;
  icon: string | null;
  comparisonCount: number;
  topComparisons: RelatedComparison[];
  subcategories: { slug: string; name: string; count: number }[];
}

