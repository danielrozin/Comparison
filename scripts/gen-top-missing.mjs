function makeSlug(a, b) {
  const sa = a.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/-+$/g, "");
  const sb = b.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/-+$/g, "");
  return `${sa}-vs-${sb}`;
}

async function tavilySearch(query) {
  try {
    const res = await fetch("https://api.tavily.com/search", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ api_key: process.env.TAVILY_API_KEY, query, max_results: 3 }),
    });
    if (!res.ok) return "";
    const data = await res.json();
    return (data.results || []).map(r => `- ${r.content?.slice(0, 300)}`).join("\n");
  } catch { return ""; }
}

async function generateOne(entityA, entityB, category) {
  const slug = makeSlug(entityA, entityB);
  const existing = await prisma.comparison.findUnique({ where: { slug } });
  if (existing) {
    console.log("skip " + slug);
    return true;
  }

  console.log("generating " + entityA + " vs " + entityB);

  const context = await tavilySearch(entityA + " vs " + entityB + " comparison 2026");

  const prompt = "You are an expert comparison writer. Generate a comprehensive, SEO-optimized comparison between " + entityA + " and " + entityB + ".\n\nRecent context:\n" + (context || "No recent context.") + "\n\nReturn JSON with fields: title, metaTitle (under 60 chars), metaDescription (120-155 chars), shortAnswer (2-3 sentences), keyDifferences (array of 4 strings), verdict (paragraph). Return ONLY JSON.";

  const response = await anthropic.messages.create({
    model: "claude-haiku-4-5-20251001",
    max_tokens: 1000,
    messages: [{ role: "user", content: prompt }],
  });

  const text = response.content[0].text;
  let data;
  try {
    const jsonMatch = text.match(/\{[\s\S]*\}/);
    data = JSON.parse(jsonMatch[0]);
  } catch (e) {
    console.error("parse error for " + slug + ": " + e.message);
    return false;
  }

  await prisma.comparison.create({
    data: {
      slug,
      title: data.title || (entityA + " vs " + entityB),
      metaTitle: data.metaTitle || (entityA + " vs " + entityB + " - Comparison"),
      metaDescription: data.metaDescription || ("Compare " + entityA + " and " + entityB),
      shortAnswer: data.shortAnswer || "",
      keyDifferences: data.keyDifferences || [],
      verdict: data.verdict || "",
      category: category || "technology",
      isAutoGenerated: true,
      status: "published",
      publishedAt: new Date(),
    }
  });

  console.log("saved: " + slug);
  return true;
}

async function main() {
  console.log("Generating " + MISSING_PAIRS.length + " high-priority comparisons...");
  let success = 0;
  for (const [a, b, cat] of MISSING_PAIRS) {
    try {
      const ok = await generateOne(a, b, cat);
      if (ok) success++;
    } catch (e) {
      console.error("Error for " + a + " vs " + b + ": " + e.message);
    }
    await new Promise(r => setTimeout(r, 1000));
  }
  console.log("Done: " + success + "/" + MISSING_PAIRS.length);
  await prisma.$disconnect();
}

main().catch(console.error);
